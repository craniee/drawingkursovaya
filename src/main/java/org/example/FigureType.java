package org.example;

/**
 * Перечисление типов геометрических фигур, поддерживаемых приложением.
 * Используется для фильтрации и выбора фигур генерации рисунка.
 */
public enum FigureType {
    /**
     * Прямая линия между двумя точками.
     */
    LINE("Линия"),

    /**
     * Окружность, заданная центром и радиусом.
     */
    CIRCLE("Окружность"),

    /**
     * Прямоугольник, заданный центром и шириной/высотой.
     */
    RECTANGLE("Прямоугольник"),

    /**
     * Равнобедренный треугольник, направленный вверх.
     */
    TRIANGLE("Треугольник"),

    /**
     * Парабола вида y = a·(x - x₀)² + y₀, направленная вверх.
     */
    PARABOLA("Парабола"),

    /**
     * Равнобедренная трапеция с параллельными основаниями по горизонтали.
     */
    TRAPEZOID("Трапеция");

    /**
     * Отображаемое название типа фигуры для GUI.
     */
    private final String label;

    /**
     * Конструктор перечисления.
     * @param label читаемое название фигуры для отображения в интерфейсе
     */
    FigureType(String label) {
        this.label = label;
    }

    /**
     * Возвращает локализованное название фигуры.
     * @return строка — метка для отображения (например, "Окружность")
     */
    public String getLabel() {
        return label;
    }
}